package ifEsleStatment;

public class Swapping {
	public static void main(String[] args) {
		int a =10;
		int b =20;
		int c;
		c=a;
		a=b;
		b=c;
		
		System.out.println("A value"+a);
		System.out.println("B value"+b);
		
		a=10;
		b=20;
		
		System.out.println(a+b);
		a=a+b;
		System.out.println(a-b);
		b=a-b;
		System.out.println(a-b);
		a=a-b;
		System.out.println("A value"+a);
		System.out.println("B value"+b);
		
		
		
	}
	

}
